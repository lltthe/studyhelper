package com.example.lamlethanhthe.studyhelper.DataModules;

public class Subject {

    private String name;
    private String test;

    public Subject(String name, String test) {
        this.name = name;
        this.test = test;
    }

    public String getName() {
        return name;
    }

    public String getTest() {
        return test;
    }
}
